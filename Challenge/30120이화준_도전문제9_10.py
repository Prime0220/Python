#   -무한 반복되는 while문을 사용한다.
#   - 두 수를 입력 받아서 두 수가 같으면 break문을 사용해 무한 반복문으 종료한다

c = 0

while  c == 0:
    a, b = input("두 수를 입력해주세요. (입력 예시 : 1,1 ) : ").split(",")

    if a == b :
        c = 1
        print("입력하신 두수 a :", a, "와 b :", b, "는 같습니다. ")

