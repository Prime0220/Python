############################# [도전문제 10] ################################
#
# - 다음 조건을 만족하는 프로그램을 작성하세요.
#   -1부터 50까지의 수를 출력한다.단, 7의 배수는 출력하지 않는다. 
#   - continue문을 사용해 문제르 해결한다.

num = 0

while num < 50 :
    num += 1
    if num%7 != 0 :
        print(num)

# - continue

num1 = 0

while num1 < 50:
        num1 += 1
        if num1 % 7 == 0 :
             continue
        print(num1)


    
